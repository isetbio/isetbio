function simulationParams(app)

    % vcSESSION
    global vcSESSION
    if (isempty(vcSESSION))
        ieInitSession;
    end
    
    % Where to export data
    app.exportDirectoryName = fullfile(strrep(sprintf('%s', isetRootPath), 'isettools', ''), ...
        'demoApps/CSFGenerator2/scripts/exportedPipelines');

    % Index of PDF output. Each time an export is requested this is
    % increased by 1
    app.pdfIndex = 1;
    
    % Debug via visualization flags
    app.debugParams = struct(...
        'visualizeConeMosaicActivationComponents', ~true ...
    );
    
    % Region of interest params
    app.roiParams = struct(...
        'fieldOfViewDegs', 3.0, ...
        'polarEccentricityDegs', 345, ...
        'radialEccentricityDegs', 3.5, ...
        'radialEccentricityScaling', 'linear', ...
        'whichEye', 'right eye', ...
        'magnificationFactorMicronsPerDeg', Inf ...
    );

    % Cone mosaic params
    app.coneMosaicParams = struct(...
        'eccVaryingMacularPigmentDensity', true, ...
        'eccVaryingMacularPigmentDynamic', ~true, ...
        'eccVaryingConeAperture', true, ...
        'eccVaryingConeApertureBlur', ~true, ...
        'eccVaryingOuterSegmentLength', true, ...
        'tritanopicRadiusDegs', 0.15, ...
        'lConeRatio', 0.6, ...
        'mConeRatio', 0.3, ...
        'sConeRatio', 0.1, ...
        'integrationTimeSeconds', 125/1000 ...
        );
    
    % Optics params
    app.opticsParams = struct(...
        'subjectDataset', 'Polans2015', ...
        'subjectRank', 1, ...
        'maxPolansSubjectRank', 10, ...
        'maxArtalSubjectRank', 50, ...
        'pupilDiameterMM', 3, ...
        'visualizedWavelength', 550, ...
        'wavefrontSpatialSamples', 301, ...
        'flipPSFUpsideDown', true, ...
        'zeroCenterPSF', true ...
        );

    % Stimulus params
    app.stimParams = struct(...
        'presentationMode', 'flashed', ...
        'durationSec', 50/1000, ...
        'temporalFrequencyHz', 4, ...
        'spatialEnvelope', 'disk', ...
        'orientationDegs', 90, ...
        'spatialFrequencyCPD', 3.0, ...
        'spatialPhaseDegs', 0.0, ...
        'sizeDegs', 2.0, ...
        'resolutionPixels', 300, ...
        'minPixelsNumPerCycle', 11, ...
        'positionDegs', [0 0], ...
        'mosaicCenteredPosition', true, ...
        'meanLuminanceCdM2', 30, ...
        'LconeContrast', 100, ...
        'MconeContrast', 100, ...
        'SconeContrast', 100, ...
        'wavelengthSupportMin', 400, ...
        'wavelengthSupportMax', 700, ...
        'wavelengthSupportStepSize', 25);

    % Psychometric function estimation params
    app.psychometricFunctionParams = struct(...
        'classifierType', 'ideal observer (Poisson)', ...
        'trainingTrials', 1, ...
        'testTrials', 128, ...
        'contrastLevels', 5, ...
        'log10ContrastMin', -5, ...
        'log10ContrastMax', 0, ...
        'log10ContrastDelta', 0.05, ...
        'slopeMin', 8, ...
        'slopeMax', 80, ...
        'slopeDelta', 2, ...
        'estimationMethod', 'adaptive (mQuest+)' ...
        );
    
    app.csfParams = struct(...
        'spatialFrequencyMin', 2, ...
        'spatialFrequencyMax', 40, ...
        'spatialFrequencySamples', 4, ...
        'constantParameter', 'constant size', ...
        'numberOfConstantCycles', 0.56, ...  % cycles/sigma. 0.56 gives a bandwidth of 1.0 octaves, as in Watson (2005), ModellFest stimuli #11-14
        'sourceSignal', 'cone excitations' ...
        );
    
    % View modes for different components
    app.viewModes = struct(...
        'coneMosaic', 'cone types', ...
        'coneMosaicActivationType', 'noise-free responses', ...
        'coneMosaicVisualizationDomain', 'degrees', ...
        'coneMosaicActivationSignal', 'excitations', ...
        'coneMosaicActivationDimensionality', '2D space', ...
        'opticsKeepConeMosaicActivationInSync', false ...
        );
    
    app.constants = struct(...
        'WatsonPyramidOfVisibilityGaborConstantSize',   [1.739 -0.060 0.391], ...
        'WatsonPyramidOfVisibilityGaborConstantCycles', [1.380 -0.091 0.391] ...
        );
end
