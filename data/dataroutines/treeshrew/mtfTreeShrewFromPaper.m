% Return a struct with treeshrew csfData from a particular paper
function mtfData = mtfTreeShrewFromPaper(paperName)

switch (paperName)
    case 'SaidakEtAl_2019'
        mtfData = SaidakEtAl2019MTFdata();
    otherwise
        error('Unknown paper: ''%s''.', paperName);
end

end

