% Return a struct with treeshrew csfData from a particular paper
function csfData = csfTreeShrewFromPaper(paperName)

switch (paperName)
    case 'casagrande84'
        csfData = Casagrande84CSFdata();
    otherwise
        error('Unknown paper: ''%s''.', paperName);
end

end

